// @lc code=start
/**
 * Definition for singly-linked list.
 * class ListNode {
 *     val: number
 *     next: ListNode | null
 *     constructor(val?: number, next?: ListNode | null) {
 *         this.val = (val===undefined ? 0 : val)
 *         this.next = (next===undefined ? null : next)
 *     }
 * }
 */

function swapPairs(head: ListNode | null): ListNode | null {
  if (!head) return null;
  let pre = head;
  let curr = head.next;
  if (!curr) {
    return pre;
  }

  let next = curr.next; // 临时保存next
  curr.next = pre; // 第二个元素指向第一个元素
  pre.next = swapPairs(next); // 第一个元素指向后一对的 头节点
  return curr; // 返回头节点
}
